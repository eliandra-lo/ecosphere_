<p>The Philippines is home to over 700 threatened species, making it one of the world’s most important conservation sites.</p>

<h2>Natural land habitats in the Philippines</h2>
<p>A wide range of ecosystems, landscapes, and habitats support the Philippines’ unique biodiversity. Many of these are threatened by human activity. Forest ecosystems cover 7.2 million hectares in the Philippines, accounting for almost a quarter of the country’s total land area. However, it’s estimated that the Philippines lost 2.1% of its forest cover each year between 2000 and 2005 – the second-fastest deforestation rate in Southeast Asia and the ninth-fastest in the world.</p>

<p>The agricultural ecosystem of the country is also noteworthy. Rice, coconut, mung beans, taro, yam, and bananas are all grown in the Philippines, and each of these agricultural areas has its own ecology. However, agricultural biodiversity is on the decline, as is the amount of land dedicated to these activities.</p>

<h2>The Philippines’ Underwater Ecosystem</h2>
<p>Inland water biodiversity is following a similar pattern, with data revealing a decline in water quality and biodiversity in the country’s largest lake – Laguna de Bay – and its associated rivers.</p>

<p>The Philippines has a diverse coastal, marine, and island ecosystem. It’s located within the Coral Triangle, which is home to the world’s greatest marine biodiversity. This environment, however, is under grave threat. While mangrove cover has increased slightly in recent years, reef cover, seagrass cover, and fisheries production have all decreased significantly.</p>

<h2> Causes of species endangerment in the Philippines </h2>
<p> Species endangerment and extinction is a complex phenomenon with many interwoven causes. These are the main ones. </p>

<!-- Info Content w/ image Content -->
<div class="text-section2">
        <h2><i>1. Exotic Pet Trade</i></h2>
        <p>Endangered species are often captured by poachers looking to profit from the lucrative trade in exotic pets. Many don’t even make it to their prospective owners, but instead die in transit.</p>
</div>

<div class="text-section2">
        <h2><i>2. Hunting and Overfishing</i></h2>
        <p>Many endangered species are pushed to the edge of extinction because they’re hunted for their meat. In some cases, locals confuse the endangered species with a more common one – for example, getting the endangered Philippine freshwater crocodile mixed up with the much more common saltwater crocodile. Ocean environments are damaged when fishermen use dynamite in an attempt to increase their catch.</p>
    </div>

<div class="text-section2">
        <h2><i>3. Deforestation and agricultural land clearing</i></h2>
        <p>Deforestation leads to the extinction of animals and plants on a massive scale, as the animals lose their habitat and protective cover. Forests and meadows are cleared to make way for the growing of crops such as rice.</p>
</div>

<div class="text-section2">
        <h2><i>4. Climate change</i></h2>
        <p>Climate change will have long-term consequences for species as expected changes in temperature, rainfall, and seasonality will cause shifts in tree species distribution and therefore habitat. There may also be changes in migration patterns and more natural disasters such as typhoons. Underwater, rising water temperatures cause coral bleaching, affecting both coral reefs and the species who rely on them as a habitat.</p>
</div>

<div class="text-section2">
        <h2><i>5. Pollution</i></h2>
        <p>Plastic pollution is a major contributor to the decline in species in the Philippines. Plastics end up in the ocean where they are eaten by birds and sea creatures, causing injury and death.</p>
